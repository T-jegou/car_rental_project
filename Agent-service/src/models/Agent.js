const mongoose = require('mongoose');
const { Schema } = mongoose;

const agentSchema = new Schema({
  name: {
    type: String,
    required: true
  },
  surname: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  }
});

module.exports = {
    agentSchema: agentSchema
} 